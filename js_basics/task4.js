// 4.1 area of a circle
const circleRadius = 15;
const circleArea = Math.PI * (Math.pow(circleRadius, 2));
console.log(+circleArea.toFixed(2));

// 4.2 area of a square
const lengthSquare = 13.7;
const widthSquare = 19.6;
const squareArea = lengthSquare * widthSquare;
console.log(+squareArea.toFixed(2));

// 4.3 volume of a cylinder
const cylinderRadius = 26;
const cylinderHeight = 8;
const volumeCylinder = Math.PI * (Math.pow(cylinderRadius, 2)) * cylinderHeight;
console.log(+volumeCylinder.toFixed(2));

